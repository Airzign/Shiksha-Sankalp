    <li class="answer">Enforce transparency and accountability in program implementation by maintaining vigil over utilization of your money through the web-based “Pardarshee” system. (This system is currently under development.)
</li>
    <li class="answer">Donors can volunteer with Shiksha Sankalp and its Partner NGOs for any of the following activities:
        <ul>
